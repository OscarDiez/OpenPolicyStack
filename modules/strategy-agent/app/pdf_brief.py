"""
pdf_brief.py — Professional PDF brief generator for IA outputs.

Produces a structured 3-section PDF:
  Page 1: Executive Summary (LLM-generated narrative)
  Page 2: Full MCDA Scoring Table (all options x all criteria)
  Page 3: Evidence & Assumptions (provenance payload)

Uses reportlab — pure Python, no system dependencies.
"""

